    public void updateTimeslotStatus() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/iqrahospital", "root", "");
            String updateStatement = "UPDATE patientdata SET patientStatus = 'checked' WHERE day = ?";
            PreparedStatement ptst = conn.prepareStatement(updateStatement);
            ptst.setString(1, prevDate);
            ptst.executeUpdate();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Record Not Found!");
        }
    }

