    private static final String PREF_KEY_PREFIX = "task_executed_for_day_";

    public void schedular() {// Get the current day name
        String currentDay = getDay();

        // Check if the task has already been executed for this day
        Preferences prefs = Preferences.userRoot().node(this.getClass().getName());
        boolean taskExecuted = prefs.getBoolean(PREF_KEY_PREFIX + currentDay, false);

        if (!taskExecuted) {
            updateTimeslotStatus();
            System.out.println("Task executed on " + currentDay);

            // Mark the task as executed for this day
            prefs.putBoolean(PREF_KEY_PREFIX + currentDay, true);
        }

    }

